public class OperatorsDemo {
    public static void main(String[] args) {

        int x = 10;
        int y = 3;

        System.out.println(x + y);
        System.out.println(x - y);
        System.out.println(x * y);
        System.out.println(x / y);
        System.out.println(x % y);

        // Order of operations
        double result = 2 + 4 * (9 - 5) / 2.0;
        System.out.println(result);
    }
}
